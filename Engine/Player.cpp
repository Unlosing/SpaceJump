void Player::Init(int x_in, int y_in, int vx_in, int vy_in)
{
	x += x_in;
	y += y_in;
	vx += vx_in;
	vy += vy_in;
}

void Player::Update()
{
	x += vx;
	y += vy;

	const int right = x + width;
	if (x < 0)
	{
		x = 0;
		vx = -vx;
	}
	else if (right >= Graphics::ScreenWidth)
	{
		x = (Graphics::ScreenWidth - 1) - width;
		vx = -vx;
	}
}

